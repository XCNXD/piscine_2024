#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	s;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-nb);
	}
	else
	{
		s = nb + 48;
		write(1, &s, 1);
	}
}
